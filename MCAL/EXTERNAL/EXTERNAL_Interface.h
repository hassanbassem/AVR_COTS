#ifndef INCLUDE_MCAL_INTERRUPTS_EXTERNAL_EXTERNAL_INTERFACE_H_
#define INCLUDE_MCAL_INTERRUPTS_EXTERNAL_EXTERNAL_INTERFACE_H_

typedef enum
{
	INT0,
	INT1,
	INT2
}INTS;

typedef enum
{
	EXTERNAL_DISABLE,
	EXTERNAL_ENABLE
}EXTERNAL_STATUS;

typedef enum
{
	LOW,
	LOGICAL_CHANGE,
	FALLING,
	RISING
}SENSE_MODE;

void MEXTERNAL_voidSetGIE(EXTERNAL_STATUS A_STATUS);
void MEXTERNAL_voidSetPIE(INTS A_INT, EXTERNAL_STATUS A_STATUS);
void MEXTERNAL_voidSetSenseMode(INTS A_INT, SENSE_MODE A_SENSE_MODE);
void MEXTERNAL_voidClearFlag(INTS A_INT);
void MEXTERNAL_voidSetCallback(INTS A_INT, void*(ptrToFunction)(void));

#endif /* INCLUDE_MCAL_INTERRUPTS_EXTERNAL_EXTERNAL_INTERFACE_H_ */
